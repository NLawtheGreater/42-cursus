#include "libft.h"
#include <unistd.h>

void	ft_putnbr_fd(int nb, int fd)
{
	char	buf;

	if (nb != -2147483648)
	{
		if (nb < 0)
		{
			write(1, "-", 1);
			nb *= -1;
		}
		if (nb >= 10)
			ft_putnbr_fd(nb / 10, fd);
		buf = '0' + (nb % 10);
		write(fd, &buf, 1);
	}
	else
		write(fd, "-2147483648", 11);
}